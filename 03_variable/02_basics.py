# Python Supports Built In Data Type:

# integer(int)-->Whole Numbers(e.g, 10,-2)
# floats(float)-->This Is Decimal Number(e.g, 10.2,22.2)
# String(str)-->This Is Text Data (e.g, "Hello","Why")
# Boolean(bool)-->This Is Represent True Or False
# list-->This Is Order Mutable collection (e.g,[1,2,3,4])
# Tuple-->This Order Immutable Collection(e.g,(1,2,3))
# sets--> This Is Unordered mutable Collection Of Element(e,g,{1,2,3,4})
# Dictionaries-->This Is Key Value Pair Collection 

age=39
print(f"This is Your Age {age}")
print(f"Type Of Age {type(age)}")

cgp=8.9
print(f"This is Your Cgp {cgp}")
print(f"Type Of Cgp {type(cgp)}")

name="SadiQ"
print(f"This Is Your Name {name}")
print(f"Type Of Name {type(name)}")

